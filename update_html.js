function esc(s) {
    return String(s).replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
}

function norm(s) {
    return s.replace(/[^\u4e00-\u9fa5a-zA-Z0-9]/g,'').toLowerCase();
}

const seen = new Set();
const merged = [];

for (const [w, h] of weibo) {
    const k = norm(w);
    if (!seen.has(k)) { seen.add(k); merged.push([w, h, '微博']); }
}
for (const [w, h] of baidu) {
    const k = norm(w);
    if (!seen.has(k)) { seen.add(k); merged.push([w, h, '百度']); }
}

merged.sort((a, b) => b[1] - a[1]);
const top100 = merged.slice(0, 100);

